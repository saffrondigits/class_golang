package main

import (
	"fmt"
)

func main() {
	// 5 is integer
	fmt.Println(5)

	// 77 is integer
	fmt.Println(77)

	// -23 is also integer 'int'
	fmt.Println(-23)

	// 9.5 is a float
	fmt.Println(9.55)

	// a is a byte
	fmt.Println(('a'))

	// "Hello World!" is a string
	fmt.Println("Hello World!")

	// true is bool, true/false
	fmt.Println(true)
}

package main

import "fmt"

func main() {
	var myInt int // Declaring myInt variable which is of integer type

	myInt = 99 // Assigning value 5 to myInt

	fmt.Println(myInt) // Printing the value of myInt variable on the standard output

	var myString string

	myString = "Some string value"

	fmt.Println(myString)

	var myFloat float32

	myFloat = 23.2

	fmt.Println(myFloat)
}
